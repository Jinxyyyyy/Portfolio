# Program that returns even numbers between two chosen numbers inclusively

def even_numbers (a,b):
    even = []
    if a > b:
        print("The first number must be lower than the second")
    else:
        for i in range (a,b + 1):
            if i % 2 == 0:
               even.append(i)
        return even

even_numbers(3,10)
